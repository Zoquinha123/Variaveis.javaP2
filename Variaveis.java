public class Variaveis {

    public static void main(String[] args) {
        int segundaSemana = 5;
        int terceiraSemana = 2;
        int quartaSemana = 7;

        System.out.println("A segunda semana tem o valor: " + segundaSemana);
        System.out.println("A terceira semana tem o valor: " + terceiraSemana);
        System.out.println("A quarta semana tem o valor: " + quartaSemana);
    }
}